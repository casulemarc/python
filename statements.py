# if, elif and else statements
hungry = True
if hungry:
    print("I'm hungry")
else:
    print("I'm not hungry")

loc = "Auto shop"
if loc == "Auto shop":
    print("Fixing his car")
elif loc == "Bank":
    print("He was at the bank")
else:
    print("I don't know")

